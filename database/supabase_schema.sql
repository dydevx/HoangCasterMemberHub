create or replace function public.set_updated_at()
returns trigger as $$
begin
  new.updated_at = now();
  return new;
end;
$$ language plpgsql;

create table if not exists public.member_users (
  id integer generated by default as identity primary key,
  name text not null,
  email text not null unique,
  password_hash text not null,
  password_salt text not null,
  role text not null check (role in ('admin', 'owner', 'customer')),
  status text not null default 'active' check (status in ('active', 'locked')),
  phone text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.shops (
  id integer generated by default as identity primary key,
  name text not null,
  slug text unique,
  address text,
  phone text,
  email text,
  logo_data_url text,
  description text,
  owner_id integer references public.member_users(id) on delete set null,
  status text not null default 'active' check (status in ('active', 'locked')),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.store_users (
  id integer generated by default as identity primary key,
  store_id integer not null references public.shops(id) on delete cascade,
  user_id integer not null references public.member_users(id) on delete cascade,
  role text not null default 'store_owner',
  created_at timestamptz not null default now(),
  unique (store_id, user_id)
);

create table if not exists public.customers (
  id integer generated by default as identity primary key,
  user_id integer not null references public.member_users(id) on delete cascade,
  shop_id integer not null references public.shops(id) on delete cascade,
  name text not null,
  slug text,
  email text,
  phone text,
  birthday date,
  address text,
  status text not null default 'active' check (status in ('active', 'locked')),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (shop_id, slug),
  unique (shop_id, user_id)
);

create table if not exists public.services (
  id integer generated by default as identity primary key,
  shop_id integer not null references public.shops(id) on delete cascade,
  name text not null,
  price integer not null default 0,
  duration_minutes integer not null default 30,
  description text,
  status text not null default 'active' check (status in ('active', 'inactive')),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.membership_cards (
  id integer generated by default as identity primary key,
  customer_id integer not null references public.customers(id) on delete cascade,
  shop_id integer not null references public.shops(id) on delete cascade,
  card_number text not null unique,
  points integer not null default 0,
  tier text not null default 'Silver' check (tier in ('Silver', 'Gold', 'Platinum', 'Diamond')),
  issued_at timestamptz not null default now(),
  expires_at date,
  status text not null default 'active' check (status in ('active', 'locked', 'expired')),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.transactions (
  id integer generated by default as identity primary key,
  customer_id integer not null references public.customers(id) on delete cascade,
  shop_id integer not null references public.shops(id) on delete cascade,
  service_id integer references public.services(id) on delete set null,
  amount integer not null default 0,
  points_delta integer not null default 0,
  note text,
  created_at timestamptz not null default now()
);

create table if not exists public.promotions (
  id integer generated by default as identity primary key,
  shop_id integer not null references public.shops(id) on delete cascade,
  title text not null,
  description text,
  discount_percent integer not null default 0,
  start_date date,
  end_date date,
  status text not null default 'active' check (status in ('active', 'inactive')),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.products (
  id uuid primary key default gen_random_uuid(),
  legacy_type text,
  legacy_id integer,
  shop_id integer references public.shops(id) on delete set null,
  title text not null,
  slug text not null unique,
  category text not null default 'service',
  summary text,
  description text,
  price numeric(12, 2),
  currency text not null default 'VND',
  image_url text,
  is_published boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (legacy_type, legacy_id)
);

