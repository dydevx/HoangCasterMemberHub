  10000 + n,
  1000 + ((n - 1) % 200) + 1,
  c.shop_id,
  1000 + ((n - 1) % 100) + 1,
  s.price,
  case when n % 8 = 0 then s.price / 10 else 0 end,
  0,
  s.price - case when n % 8 = 0 then s.price / 10 else 0 end,
  (s.price / 10000)::integer,
  'Generated transaction',
  now() - ((n % 180) || ' days')::interval
from generate_series(1, 1000) as n
join public.customers c on c.id = 1000 + ((n - 1) % 200) + 1
join public.services s on s.id = 1000 + ((n - 1) % 100) + 1
on conflict (id) do nothing;

